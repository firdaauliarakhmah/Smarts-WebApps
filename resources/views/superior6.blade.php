          <center>
          <h2>Beasiswa Kuliah di Luar Negeri Tanpa TOEFL/IELTS</h2><br>
            <img src="img/rekom/f7.png" alt="" class="img-fluid services-img"> </center>
            <br>
            <p class="justify">
                Ada beberapa negara yang memberikan beasiswa tanpa adanya tes TOEFL sebagai syaratnya, 
                loh! Penasaran kan apa saja program beasiswa yang diberikan dan beasiswa apa yang tepat buat kalian? Cari tahu yuk!
            </p>
            <h5>1. Brunei Darussalam Goverment Scholarship (BDGS)</h5>
            <p class="justify">
            Beasiswa Brunei Darussalam merupakan program yang didanai secara penuh oleh pemerintah Brunei 
            Darussalam. Beasiswa ini menawarkan program kuliah untuk jenjang D3, S1 maupun S2 bagi mahasiswa 
            yang berasal dari negara anggota Organisasi Kerjasama Islam (OKI), ASEAN, Persemakmuran, dan 
            asosiasi lainnya. BDGS menjadi pogram beasiswa yang cukup populer dan dibuka setiap tahunnya. 
            Fasilitas beasiswa mencakup biaya kuliah hingga biaya hidup selama tinggal di Brunei. Selain itu, 
            bagi mahasiswa internasional akan mendapat peluang untuk mengakses pekerjaan secara global di masa depan.
            </p>
            <p class="justify">
                Cakupan Beasiswa darusalam ini sebagai berikut : 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Pembebasan biaya kuliah selama masa studi di Brunei Darussalam</span></li>
              <li><i class="bi"></i> <span>Tunjangan hidup per bulan sebesar BND$500,oo ( ± Rp 5.3 jt)</span></li>
              <li><i class="bi"></i> <span>Tiket pesawat ekonomi PP (awal dan akhir masa perkuliahan)</span></li>
              <li><i class="bi"></i> <span>Uang tunjangan buku BND$600,00 ( ± Rp 6.3 jt)</span></li>
              <li><i class="bi"></i> <span>Uang makan per bulan BND$150,00 ( ± Rp1.5 jt)</span></li>
              <li><i class="bi"></i> <span>Akomodasi tempat tinggal atau asrama</span></li>
              <li><i class="bi"></i> <span>Biaya asuransi kesehatan selama masa studi</span></li>
              <li><i class="bi"></i> <span>Biaya bagasi untuk pengiriman barang pribadi penerima beasiswa, dengan jumlah maksimum BND$250,00 ( ± Rp 2.6 jt)</span></li>
            </ul>

            <p class="justify">
                Persyaratan yang harus diperhatikan : 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Warga negara ASEAN, OIC, atau Commonwealth</span></li>
              <li><i class="bi"></i> <span>Usia maks S1 25 tahun & S2-S3 maks 35 tahun</span></li>
              <li><i class="bi"></i> <span>Bukan merupakan warga negara Brunei</span></li>
            </ul>

            <br>
            <h5>2. Romanian Government Scholarship</h5>
            <p class="justify">Romanian Government Scholarship adalah program beasiswa yang diselenggarakan 
              oleh Pemerintah Rumania melalui Ministry of Foreign Affairs (MFA) untuk membiayai mahasiswa 
              internasional dari luar Eropa yang ingin melanjutkan kuliah di universitas di Rumania.
              Dalam rangka mempromosikan bahasa dan budaya Rumania, maka seluruh perkuliahan kecuali 
              pada program doktoral akan disampaikan dalam bahasa Rumania. Penerima beasiswa yang belum 
              mahir berbahasa Rumania dapat mengikuti kelas pelatihan bahasa Rumania selama 1 tahun sebelum 
              memasuki perkuliahan. Akan tetapi beasiswa ini tidak berlaku untuk mendaftar di jurusan 
              kedokteran, kedokteran gigi, dan farmasi.
            </p>
            <p class="justify">Tertarik kuliah S-1, S-2, atau S-3, di negara yang terkenal dengan 
              arsitektur unik? Kamu bisa mencoba daftar beasiswa penuh untuk kuliah di Rumania, 
              salah satu negara yang berlokasi di Eropa Tengah dan Tenggara.
              Pemerintah Rumania melalui Kementerian Luar Negeri Rumania menawarkan beasiswa untuk 
              warga negara asing yang ingin kuliah di Rumania. Selama kamu bukan merupakan warga 
              negara Uni Eropa, kamu bisa mendaftar pada program beasiswa ini dan tidak perlu 
              melampirkan sertifikat kemampuan berbahasa Inggris.Jika menjadi penerima beasiswa ini, kamu akan mendapatkan fasilitas berupa:
          
            
            <ul>
              <li><i class="bi"></i> <span>Biaya studi</span></li>
              <li><i class="bi"></i> <span>Akomodasi dan asrama</span></li>
              <li><i class="bi"></i> <span>Uang saku bulanan (65 Euro/ sekitar Rp 979.000 untuk mahasiswa S-1, 75 Euro/ sekitar Rp 1.129.000 untuk mahasiswa S-2, dan 85 Euro/ sekitar Rp 1.280.000 untuk mahasiswa S-3)</span></li>
            </ul></p>

            <p class="justify">Kriteria Penerima Romanian Government Scholarship :  </p>
            <ul>
              <li><i class="bi"></i> <span>Bukan merupakan warga negara di lingkup Uni Eropa</span></li>
              <li><i class="bi"></i> <span>Pemohon beasiswa tidak boleh memegang kewarganegaraan Rumania, tidak meminta atau tidak mendapatkan bentuk perlindungan di Rumania, tidak tinggal menetap di Rumania, dan</span></li>
              <li><i class="bi"></i> <span>tidak memiliki keluarga yang bekerja di Rumania</span></li>
              <li><i class="bi"></i> <span>Belum mendapatkan beasiswa lain dari pemerintah Rumania pada program studi yang sama.</span></li>
            </ul>

            <br>
            <h5>3. Turkiye Burslari Scholarship</h5>
            <p class="justify">Beasiswa Turki Turkiye Burslari adalah beasiswa untuk program S1, 
              S2, S3, penelitian, hingga program bahasa Turki. Beasiswa YTB Turki tidak hanya 
              memberikan bantuan biaya pendidikan dan biaya hidup saja, tapi juga menjamin penempatan 
              universitas Hunters nanti.</p>
              <p class="justify">
              Bukan hanya itu, dengan jaringan alumni dunia dan program pengembangan diri yang ditawarkan, 
              Beasiswa Turki Turkiye Burslari memberikan pengalaman yang belajar yang berbeda dari pada 
              yang lain. Beasiswa Turki Turkiye Burslari diberikan oleh Presidency for Turks Abroad and 
              Related Communities (YTB) dan bekerja sama dengan institusi lain seperti YÖK, Yurt–Kur, 
              General Directorate of Migration Management dan Ministry of Foreign Affairs. Hingga awal 
              berdiri, Beasiswa YTB Turki telah memberangkatkan ribuan mahasiswa dari 178 negara dunia.</p>
            <p>Jenis-jenis program yang ditawarkan : </p>

            <p class="justify">1. Full-time program beasiswa Turki Turkiye Burslari</p>
            <ul>
              <li><i class="bi"></i> <span>Undergraduate Program</span></li>
              <p class = "justify">Undergraduate Scholarship Program adalah beasiswa 
                yang diberikan oleh Beasiswa YTB Turki untuk calon mahasiswa sarjana 
                dari berbagai negara, salah satunya Indonesia. Program beasiswa ini 
                memberi kesempatan kepada calon mahasiswa untuk menempuh pendidikan di 
                semua program sarjana di Turki.</p>
              <li><i class="bi"></i> <span>Graduate Program</span></li>
              <p class = "justify">Graduate Scholarship Program adalah beasiswa YTB Turki 
                yang diberikan untuk level master dan doktor. Program yang ditawarkan sangat 
                luas, mulai dari Social Science, Humanities, Natural Sciences dan Engineering, 
                tidak tersedia untuk program spesialis di bidang kedokteran.</p>
              <li><i class="bi"></i> <span> Art Program</span></li>
              <p class = "justify">Art Scholarship Program memberikan kesempatan beasiswa YTB 
                Turki di tingkat sarjana, master dan doktor untuk siswa internasional di bidang 
                ilmu musik, seni pertunjukan dan seni rupa di Ankara University of Music and Fine Arts.>/p>
            </ul>

            <p class="justify">2. Short-term program beasiswa Turki Turkiye Burslari</p>
            <ul>
              <li><i class="bi"></i> <span>Diberikan untuk mahasiswa yang telah menempuh 
                pendidikan di Turki dengan biaya mandiri dan memiliki prestasi akademik yang 
                baik dan tersedia untuk jenjang S1, S2, maupun S3. Fasilitas yang ditawarkan 
                program beasiswa YTB Turki hanya mencakup uang saku per bulan.</p>

              <li><i class="bi"></i> <span>Research Scholarship Program (3-12 bulan)</span></li>
              <p class = "justify">Program ini dirancang untuk mendorong para peneliti dan akademisi 
                internasional untuk melakukan riset kolaborasi dengan akademisi Turki di universitas 
                paling bergengsi. Program ini hanya tersedia untuk mahasiswa doktoral yang berada di 
                tahap penyusunan disertasi dan hanya menyediakan fasilitas uang saku per bulan.</p>

              <li><i class="bi"></i> <span>Turkish Language Programme for Public Officials and Academicians (KATIP)</span></li>
              <p class = "justify">Program ini dirancang untuk pejabat publik asing, diplomat, 
                akademisi, dan peneliti yang ingin belajar bahasa Turki dan mengenal Turki lebih 
                dalam selama 8-10 bulan. Fasilitaskan yang diberikan oleh Beasiswa Turki Turkiye Burslari 
                mencakup uang saku bulanan dan akomodasi</p>
            </ul>
            <br>
            <h5>4. China Government Scholarship (CGS)</h5>
            <p class="justify">
            Seperti namanya, beasiswa tanpa TOEFL ini berasal dari pemerintah China 
            untuk mereka yang ingin berkuliah jenjang S-1, S-2, atau S-3 di China.
            CGS menawarkan dua skema beasiswa: penuh dan parsial. Skema beasiswa ini bergantung pada 
            cara pendaftaran. Kamu bisa mendaftarkan langsung melalui universitas tujuan maupun melalui 
            Kedutaan Besar Cina di Indonesia. Apabila mendaftar melalui universitas tujuan, jika kamu 
            beruntung mendapatkan beasiswa ini maka beasiswa yang kamu terima adalah beasiswa penuh. 
            Jika mendaftar melalui Kedutaan Besar Cina di Indonesia, kamu bisa mendapatkan beasiswa penuh 
            ataupun parsial. 
            </p>
            <p class="justify">
                Fasilitas Beasiswa CGS ini sebagai berikut : 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Biaya studi </span></li>
              <li><i class="bi"></i> <span>Biaya tempat tinggal</span></li>
              <li><i class="bi"></i> <span>Uang saku</span></li>
              <li><i class="bi"></i> <span>Asuransi kesehatan</span></li>
              <li><i class="bi"></i> <span>Biaya buku</span></li>
              <li><i class="bi"></i> <span>Fasilitas beasiswa parsial berupa satu atau beberapa dari fasilitas beasiswa penuh.</span></li>
              
            </ul>

            <p class="justify">
                Persyaratan yang harus diperhatikan : 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Warga negara ASEAN, OIC, atau Commonwealth</span></li>
              <li><i class="bi"></i> <span>Usia maks S1 25 tahun & S2-S3 maks 35 tahun</span></li>
              <li><i class="bi"></i> <span>Bukan merupakan warga negara Brunei</span></li>
            </ul>

