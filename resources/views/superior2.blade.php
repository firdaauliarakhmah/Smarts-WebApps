          <center>
          <h2> 4 Rekomendasi Beasiswa Lokal untuk Mahasiswa S1</h2><br>
            <img src="img/rekom/f4.webp" alt="" class="img-fluid services-img"> </center>
            <br>
            <p class="justify">
            Melanjutkan pendidikan di perguruan tinggi merupakan salah satu dari impian kebanyakan 
            pelajar yang ada didunia, apalagi jika diiringi dengan sebuah privilege beasiswa.
            Selain membantu meringankan beban kedua orang tua, beasiswa juga dapat menjadi suatu penyemangat 
            belajar selama berada di bangku perguruan tinggi. Sehingga dapat meningkatkan kreativitas 
            dan prestasi mahasiswa pada bidang akademik maupun non-akademik. 
            Berikut ini adalah beberapa daftar rekomendasi beasiswa S-1 yang telah kami ringkas :  
            </p>
            <ul>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Beasiswa PPA</span></li>
              <li><i class="justify" class="bi bi-check-circle"></i> <span>Beasiswa Bantuan Belajar Mahasiswa (BBM)</span></li>
              <li><i class="justify" class="bi bi-check-circle"></i> <span>Beasiswa Bank Indonesia</span></li>
              <li><i class="justify" class="bi bi-check-circle"></i> <span>Djarum Beasiswa Plus</span></li>
            </ul>
            <h5>1. Beasiswa PPA (Peningkatan Prestasi Akademik)</h5>
            <p class="justify">
              Beasiswa ini ditawarkan oleh Kementerian Riset, Teknologi, dan Pendidikan Tinggi (Kemenristedikti) 
              kepada para mahasiswa di perguruan tinggi Indonesia yang sedang menempuh jenjang pendidikan S1/D4 dengan minimal di 
              semester 2 dan maksimal semester 8. Beasiswa ini juga ditawarkan kepada para mahasiswa yang sedang menempuh 
              jenjang D3 dengan minimal di semester 2 dan maksimal semester 4.

              Adapun bantuan biaya pendidikan PPA adalah sebesar Rp 400 ribu/bulan dan Cakupan yang didapat dari 
              beasiswa ini yaitu mendapat bantuan biaya hidup sebesar Rp2,4 juta setiap semester selama satu tahun. 
              Untuk kamu yang tertarik mendaftar 
              beasiswa PPA, silakan hubungi bagian kemahasiswaan kampus atau universitas masing-masing. Hal ini dikarenakan, informasi 
              mengenai beasiswa PPA biasanya langsung diberikan kepada kampus masing-masing tanpa ada pemberitahuan 
              melalui media sosial. Adapun beberapa syarat yang harus dipenuhi seperti : 
            </p>
            <ul>
              <li><i class="check-circle"></i> <span>Jenjang S1/Diploma IV minimal semester II dan maksimal semester VIII. 
                    Sedangkan Diploma III, minimal semester II dan maksimal semester VI. </span></li>
              <li><i class="check-circle"></i> <span>IPK minimal 3.00 </span></li>
              <li><i class="check-circle"></i> <span>Mengajukan permohonan tertulis kepada Rektor/Ketua/Direktur atau pejabat 
                    perguruan tinggi yang ditunjuk, dengan melampirkan berkas : fotokopi Kartu Tanda Mahasiswa (KTM), Kartu Rencana Studi (KRS), 
                    piagam atau bukti prestasi, Kartu Keluarga, surat pernyataan tidak menerima beasiswa, dan surat rekomendasi dari pimpinan 
                    Fakultas/Jurusan. </span></li>
            </ul>

            <br>
            <h5>2. Beasiswa Bantuan Belajar Mahasiswa (BBM)</h5>
            <p class="justify">Beasiswa BBM merupakan beasiswa dari Dirjen Dikti Kemdiknas, 
              tetapi ditujukan untuk membantu ekonomi mahasiswa terlepas dari prestasinya. 
              BBM tidak jauh berbeda dengan PPA dan pendaftarannya biasanya bersamaan dengan PPA.
              Beasiswa BBM sama dengan PPA, terutama tujuan dan definisnya, yaitu memberikan bantuan 
              kepada mahasiswa yang mengalami kesulitan membayar biaya pendidikannya. Sama dengan PPA, 
              tujuannya membantu meringankan beban orang tua dari kalangan ekonomi lemah. 
              Adapun syarat atau ketentuan khusus untuk mendapatkan beasiswa BBM diantaranya sebagai berikut : 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Jenjang S1/Diploma IV/Diploma III paling rendah pada semester 
                II dan paling tinggi duduk pada semester VI untuk Jenjang S1 dan Jenjang Diploma IV, paling rendah pada 
                semester II dan paling tinggi duduk pada semester IV untuk Jenjang Diploma III</span></li>
              <li><i class="bi"></i> <span>IPK minimal 3.00 </span></li>
              <li><i class="bi"></i> <span>Berasal dari keluarga tidak mampu secara ekonomi 
                (diutamakan mahasiswa yang terdata pada DTKS Kemensos)</span></li>
              <li><i class="bi"></i> <span>Tidak sedang menerima beasiswa, bekerja dan atau berada 
                dalam status ikatan dinas dari lembaga/Instansi lain</span></li>
            </ul>

            <br>
            <h5>3. Beasiswa Bank Indonesia</h5>
            <p class="justify"> Beasiswa ini ditawarkan oleh Bank Indonesia (BI) kepada para mahasiswa 
                  di beberapa perguruan tinggi negeri dan juga swasta di Indonesia yang telah menempuh dan menyelesaikan minimal 
                  40 SKS atau minimal sedang menempuh perkuliahan di semester tiga.
                  Cakupan yang bisa didapat dari beasiswa Bank Indonesia ini adalah tunjangan biaya hidup sebesar
                  Rp500 ribu sampai Rp750 ribu setiap bulannya. Para penerima beasiswa BI ini membuat komunitas 
                  yang bernama GenBI (Generasi Baru Indonesia).

                  Beasiswa BI terdiri dari 2 jenis, yaitu Beasiswa Reguler dan Beasiswa Unggulan. 
                  Perbedaannya terletak pada syarat pendaftarannya dan biaya bulanan yang didapatkan. 
                  Syarat untuk melamar beasiswa ini adalah : 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Telah menempuh 40 SKS (menjalani minimal 3 semester) </span></li>
              <li><i class="bi"></i> <span>IPK minimal 3.00 </span></li>
              <li><i class="bi"></i> <span>Aktif dalam kegiatan sosial yang berdampak bagi masyarakat</span></li>
              <li><i class="bi"></i> <span>Berasal dari keluarga kurang mampu </span></li>
              <li><i class="bi"></i> <span>Mendapat rekomendasi dari pimpinan perguruan tinggi dan tokoh akademik/ non akademik </span></li>
              <li><i class="bi"></i> <span>Memiliki sertifikat dan menyertakan resume dan motivation letter </span></li>
              <li><i class="bi"></i> <span>Memiliki sertifikat TOEFL dengan nilai minimal 500 dari institusi terpercaya (khusus untuk pendaftar Beasiswa Unggulan) </span></li>
            </ul>

            <br>
            <h5>4. Djarum Beasiswa Plus</h5>
            <p class="justify"> Beasiswa ini bernama Djarum Besiswa Plus. Beasiswa ini ditawarkan 
              oleh perusahaan besar yang telah banyak melahirkan atlet-atlet bulu tangkis yang hebat. 
              Selain memiliki beasiswa bulutangkis, Djarum juga memiliki program beasiswa untuk mahasiswa 
              di perguran tinggi Indonesia. Beasiswa ini ditawarkan kepada mahasiswa yang sedang menempuh 
              pendidikan S1/D4 di semester empat dari semua disiplin ilmu.

              Cakupan yang didapat dari beasiswa ini yaitu tunjangan dana sebesar Rp1 juta setiap bulan 
              selama 1 tahun. Selain dana beasiswa, para penerima Djarum Beasiswa Plus (Beswan Djarum) 
              juga mendapatkan pembekalan berbagai macam soft skills, guna menyerasikan antara pencapaian 
              akademik (hard skills) yang diperoleh di kampus dengan berbagai keterampilan agar para Beswan 
              Djarum di kemudian hari menjadi manusia yang cakap intelegensia dan emosional.

              Untuk mendaftar beasiswa ini, pastikan kamu juga merupakan mahasiswa yang sedang berkuliah 
              di universitas yang menjadi mitra Djarum Foundation ya, sobat. Kamu dapat mengeceknya di 
              website Djarum Foundation. Berikut ini adalah beberapa syarat yang harus dipenuhi ketika mendaftar beasiswa Djarum Plus ini :
            </p>
            <ul>
              <li><i class="bi"></i> <span>Sedang menempuh semester 4 di perguruan tinggi negeri atau swasta</span></li>
              <li><i class="bi"></i> <span>IPK minimal 3.00 </span></li>
              <li><i class="bi"></i> <span>Aktif dalam organisasi </span></li>
              <li><i class="bi"></i> <span>Tidak sedang menerima beasiswa dari pihak lain </span></li>
            </ul>

