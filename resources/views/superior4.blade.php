          <center>
          <h2>Rekomendasi Buat Kamu Yang Mau Ambil Beasiswa Di Australia</h2><br>
            <img src="img/rekom/f5.jpeg" alt="" class="img-fluid services-img"> </center>
            <br>
            <p class="justify">
            Australia merupakan salah satu negara dengan tujuan kuliah terpopuler di kalangan pelajar internasional. 
            Negara tersebut menawarkan sistem pendidikan berkualitas yang saat ini menempati posisi 
            ke-4 dalam peringkat negara dengan pendidikan terbaik di dunia. Selain itu, situasi 
            perekonomian di Australia relatif stabil dan kuat. Karenanya, Australia menawarkan peluang 
            kerja menjanjikan bagi warga negara asing.
            Australia juga dikenal sebagai negara yang tidak pelit akan beasiswa. Bahkan sejumlah program 
            beasiswa menawarkan skema biaya kuliah fully-funded dan tunjangan hidup bagi penerimanya.
            </p>
            <p class="justify"> Sebuah kebanggaan tersendiri Jika kamu dapat memperoleh beasiswa S1 
                Australia untuk untuk kepentingan studi lebih lanjut.  Tidak hanya Amerika atau Eropa, 
                Australia memiliki banyak lembaga perguruan tinggi populer yang menjadi pilihan mahasiswa 
                internasional. Hampir setiap tahun selalu ada mahasiswa internasional yang berangkat ke 
                Australia untuk keperluan pendidikan. Dengan biaya kuliah dan biaya lainnya yang harus 
                kamu pikirkan, termasuk akomodasi atau biaya hidup sehari-hari, mendaftar beasiswa S1 di 
                Australia akan sangat membantu.
                Dengan memiliki prestasi akademik memuaskan, kamu bisa mengambil peluang kuliah lebih murah 
                atau bahkan gratis dari program beasiswa. Apalagi, ada banyak program tersedia yang dapat 
                kamu manfaatkan. Berikut ini adalah beberapa program biasiswa yang bisa kamu coba: </p>
            <h5>1. Australia Awards Scholarship (AAS)</h5>
            <p class="justify">Australia Awards Scholarship (AAS) merupakan program beasiswa yang 
                diselenggarakan oleh pemerintah Australia. Beasiswa ini dibuka untuk jenjang sarjana 
                serta pascasarjana. Cakupan beasiswa ini meliputi biaya kuliah, uang saku, tiket pesawat, 
                tunjangan kedatangan, asuransi kesehatan, dan tunjangan akademik lainnya.
                Selain itu, kamu akan mendapatkan fasilitas berupa program perkenalan akademik sebelum 
                memulai masa perkuliahan di Australia. Program tersebut bertujuan untuk mengenalkan kamu 
                kepada sistem pendidikan serta kehidupan di Australia, sehingga nantinya kamu bisa beradaptasi 
                dengan lebih baik selama berada di sana. Kamu juga akan mendapatkan pendanaan untuk penelitian 
                lapangan apabila diperlukan. Berikut ini adalah jurusan yang harus diperhatikan jika kamu 
                ingin mengambil beasiswa ini: 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Ilmu Kesehatan dan Kedokteran: Kesehatan Masyarakat</span></li>
              <li><i class="bi"></i> <span>Humaniora dan Ilmu Sosial: Ilmu Hukum dan Hukum</span></li>
              <li><i class="bi"></i> <span>Keuangan dan Ekonomi: Ekonomi</span></li>
              <li><i class="bi"></i> <span>Bisnis: Administrasi Bisnis, Manajemen Bisnis, Pemasaran, 
                Manajemen Sumber Daya Manusia, Manajemen Organisasi, Rantai Pasokan dan Manajemen Logistik, 
                Manajemen Industri, Informasi</span></li>
              <li><i class="bi"></i> <span>Studi Lingkungan: Perubahan Iklim, Pengelolaan Sumber Daya Alam</span></li>
              <li><i class="bi"></i> <span>Ilmu Politik: Pemerintahan, Hubungan Internasional, Administrasi Publik, Ilmu Politik.</span></li>
            </ul>

                <a href="https://www.dfat.gov.au/people-to-people/australia-awards/australia-awards-scholarships" class="btn btn-info py-sm-1 px-sm-5 me-1">Selengkapnya...</a><br>
            
            <br>
            
            <h5>2. The Indonesian Global Leaders Scholarship (University of Queensland)</h5>
            <p class="justify">Indonesia Global Leaders Scholarship merupakan program beasiswa yang 
                dimiliki oleh University of Queensland (UQ) yang ditujukan bagi mahasiswa Indonesia 
                yang memiliki prestasi di atas rata-rata untuk berkuliah di Fakultas Ekonomi & Bisnis 
                dan Fakultas Hukum UQ.
                Beasiswa ini dibuka di awal Semester 1 dan Semester 2. Tanggal penutupan bagi 
                pendaftaran beasiswa di Semester 1 adalah di akhir bulan November.
            </p>
            <p class="justify" >Cakupan Beasiswa Indonesia Global Leaders: 
            <ul>
              <li><i class="bi"></i> <span>Beasiswa biaya pendidikan sejumlah $5000 â€“ $10,000. Jumlah total yang diberikan 
                kepada penerima beasiswa adalah di bawah wewenang panitia seleksi.</span></li>
              <li><i class="bi"></i> <span>Kesempatan untuk bergabung dalam komunitas UQ Global Leaders, yang bermanfaat 
                untuk mengembangkan skill kepemimpinan dan kesiapan untuk berkarier melalui seminar, event, dan jejaring profesional dengan sesama mahasiswa dan alumni.</span></li>
            </ul>
                <a href="https://scholarships.uq.edu.au/scholarship/indonesia-global-leaders-scholarship" class="btn btn-info py-sm-1 px-sm-5 me-1">Selengkapnya...</a></p><br>

            <h5>3. Monash International Merit Scholarships</h5>
            <p class="justify">Universitas Monash merupakan salah satu perguruan tinggi ternama di Australia. 
                Setiap tahun, universitas ini menawarkan beasiswa bagi pelajar asing melalui skema Monash 
                International Merit Scholarship. Beasiswa ini dibuka bagi mahasiswa jenjang sarjana maupun 
                pascasarjana di Universitas Monash. Sesuai namanya, kriteria penerima beasiswa ditentukan 
                oleh prestasi akademik kandidat. </p>
              <p class="justify">
              Cakupan beasiswa ini meliputi dana pendidikan sebesar 10 ribu dollar Australia 
              (per 48 SKS) per tahun. Selain itu, mahasiswa baru jenjang S1 yang menerima beasiswa 
              ini akan mendapat kesempatan untuk mengikuti program kepemimpinan Universitas Monash, 
              Monash Minds. Kamu juga memiliki kesempatan untuk menjadi duta kampus dan ambil bagian 
              dalam kegiatan promosi Universitas Monash. Persyaratan saat mendaftar beasiswa ini adalah: </p>
            <ul>
              <li><i class="bi"></i> <span>Mahasiswa internasional</span></li>
              <li><i class="bi"></i> <span>Memulai sebagai mahasiswa dengan tawaran kuliah penuh di Monash atau mahasiswa lanjutan</span></li>
              <li><i class="bi"></i> <span>Mengambil perkuliahan (coursework) penuh waktu untuk gelar sarjana atau pascasarjana di kampus Monash, Australia</span></li>
              <li><i class="bi"></i> <span>Tidak memenuhi syarat jika sedang studi di sekolah menengah 
                Australia (year 12) Pelamar diminta mempertahankan nilai 70 % atau lebih setiap semesternya 
                untuk agar bisa melanjutkan beasiswa serta mengambil beban studi penuh waktu (48 kredit poin).</span></li>
               </ul>

            <a href="https://www.monash.edu/study/fees-scholarships/scholarships/find-a-scholarship/international-merit-5770" class="btn btn-info py-sm-1 px-sm-5 me-1">Selengkapnya...</a>
            <br>

            <br>
            <h5>4. Melbourne International Research</h5>
            <p class="justify">Selain Universitas Monash, Beasiswa Australia yang tak kalah bergengsi lainnya 
              adalah Universitas Melbourne. Kampus ini juga menawarkan berbagai beasiswa menarik bagi 
              pelajar asing melalui skema Melbourne Research Scholarship. Beasiswa ini diberikan kepada 
              mahasiswa pascasarjana untuk keperluan riset akademik. 
              
              Melbourne Research Scholarship terbagi ke dalam 2 jenis, yaitu kompensasi biaya dan tunjangan. 
              Kompensasi biaya artinya kamu akan mendapatkan penghapusan biaya kuliah 100% selama 2 tahun 
              masa studi S2 atau 4 tahun masa studi S3. Sementara itu, beasiswa jenis tunjangan mencakup 
              biaya kuliah (gratis 100%), uang saku 34 ribu dollar Australia per tahun, tunjangan kepindahan 
              sebesar 2-3 ribu dollar Australia, dan asuransi kesehatan. </p>

              <p class="justify">Adapun persyaratan yang harus di perhatikan untuk 
                mengambil beasiswa di Melbourne yaitu sebagai berikut:  </p>
            <ul>
              <li><i class="bi"></i> <span>Pelamar bukan warganegara Australia atau New Zealand</span></li>
              <li><i class="bi"></i> <span>Pelamar telah mendaftar atau diterima di University of Melbourne</span></li>
              <li><i class="bi"></i> <span>Tidak mengajukan beasiswa untuk kualifikasi yang sama, kecuali sebelumnya mengambil masters by coursework dan ingin mendaftar masters by research</span></li>
              <li><i class="bi"></i> <span>Pelamar hanya dipertimbangkan untuk satu departemen/jurusan</span></li>
              <li><i class="bi"></i> <span>Lulus sarjana atau master dengan hasil memuaskan/cumlaude</span></li>
              <li><i class="bi"></i> <span>Belum pernah mendapatkan beasiswa MIRS, MRS, FMS, atau HRS</span></li>
               </ul>

            <a href="https://www.monash.edu/study/fees-scholarships/scholarships/find-a-scholarship/international-merit-5770" class="btn btn-info py-sm-1 px-sm-5 me-1">Selengkapnya...</a>
