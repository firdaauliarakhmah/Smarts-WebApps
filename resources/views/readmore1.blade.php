                            Ada pula beasiswa kuliah yang sempat ramai diperbincangkan di media sosial beberapa 
                            waktu lalu, LPDP. Beasiswa ini juga merupakan beasiswa kuliah Program Magister dan 
                            Doktoral dari pemerintah, melalui Kemenkeu. Pada intinya, beasiswa pemerintah dapat 
                            kita peroleh melalui berbagai institusi atau lembaga pemerintahan.</p>

