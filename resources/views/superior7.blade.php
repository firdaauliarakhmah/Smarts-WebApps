          <h2>Beasiswa Kuliah di Amerika Serikat</h2><br>
            <img src="img/rekom/f9.jpeg" alt="" class="img-fluid services-img"> </center>
            <br>
            <p class="justify">
            Kuliah di kampus dengan sebuah privilege kampus dengan sistem pendidikan terbaik dunia 
            yang ada di Amerika Serikat sudah pasti memakan 
            biaya yang tidak sedikit. Tapi, jangan khawatir! Ada banyak beasiswa yang bisa mengantarkanmu 
            kuliah di kampus bergengsi ini.
            </p>
            
            <p class="justify">Amerika Serikat adalah salah satu negara tujuan kuliah yang diidam-idamkan 
              banyak orang. Nggak heran, sebab AS menjadi rumah bagi ribuan universitas dengan kualitas 
              pendidikan juara dan terbaik dunia. Gelar dari kampus-kampus AS sudah diakui secara internasional, sehingga 
              tidak sulit bagi para penyandangnya untuk mencari kerja. 
              Apakah kamu termasuk salah satu yang ingin kuliah di Amerika Serikat? Tentunya, kamu bisa 
              mewujudkan cita-citamu tersebut! Berikut adalah sejumlah beasiswa kuliah di AS yang bisa 
              kamu gunakan untuk mewujudkan mimpimu!
            </p>

            <h5>1. Abbey Road Summer Scholarships</h5>
            <p class="justify">
            Beasiswa ini ditujukan untuk kamu yang masih berstatus sebagai pelajar SMA dengan usia 14 
            sampai 18 tahun. Prestasi akademikmu harus jempolan, terutama pada bidang bahasa dan seni, 
            serta punya kegiatan ekskul yang ketjeh. Beasiswa sejumlah $1,000 USD ditawarkan setiap tahun 
            untuk kategori Bahasa Perancis, Bahasa Spanyol, Bahasa Itali/ Cultural Studies, Studio Art/ 
            Sejarah Seni, dan International Studies. Beasiswa ini menawarkan $ 1000 untuk mahasiswa jurusan 
            Seni dan Bahasa, dan $ 500 untuk kelas di musim panas. Program beasiswa ini merupakan beasiswa 
            non-pemerintah.
            </p>

            <br>
            <h5>2. Foreign Fulbright Student Program</h5>
            <p class="justify"> Foreign Fulbright Student Program merupakan program beasiswa untuk S2 yang terpopuler dikalangan mahasiswa 
              internasional. Beasiswa ini dapat ditemukan di 155 negara dan setiap tahunnya menerima 4 
              ribu mahasiswa asing yang siap menempuh pendidikan di universitas yang ada di Amerika. 
              Program ini merupakan program pemerintah Amerika Serikat.
              Beasiswa ini ditawarkan untuk kamu yang ingin berkuliah S2 maupun S3 
              di Negeri Paman Sam. Cakupannya meliputi biaya kuliah, biaya hidup, asuransi kesehatan, 
              tunjangan visa, dan tiket PP Indonesia-AS atau sebaliknya. Berbekal Fulbright Scholarship, 
              kamu bisa berkuliah di kampus-kampus keren AS, seperti American University dan Kent State 
              University. 
            </p>

            <br>
            <h5>3. New York University Wagner International Scholarships</h5>
            <p class="justify">New York University punya berbagai program beasiswa untuk membantu 
              mahasiswanya. Diantaranya, program yang ditujukan untuk mahasiswa internasionall pada 
              jenjang S1 adalah Dean's Scholarship, Public Service Scholarship, Luther ataupun M. Gulick 
              Scholarship. Ada pula external scholarships yang ditawarkan oleh organisasi atau institusi 
              luar kampus bagi mahasiswa internasional New York University</p>

            <br>
            <h5>4. Global Undergraduate Exchange Program (UGRAD) </h5>
            <p class="justify">
            Beasiswa ini juga diselenggarakan oleh AMINEF, lembaga yang menawarkan Fulbright Scholarship. 
            Bedanya, beasiswa ini berbentuk program pertukaran mahasiswa di AS. Global Undergraduate 
            Exchange Program (UGRAD) diberikan kepada mahasiswa S1 yang tengah menjalani semester 3-5. 
            Selama satu semester, kamu berkesempatan untuk belajar di kampus ternama di AS. 
            Meski cuma satu semester, UGRAD mencakup biaya kuliah, uang saku, hingga asuransi kesehatan. 
            Bahkan kamu akan difasilitasi tempat tinggal gratis berupa asrama, buku referensi kuliah, 
            serta konsumsi selama berada di sana.
            </p>

            <br>
            <h5>5. American Association of Women University (AAUW)</h5>
            <p class="justify">
            Beasiswa yang satu ini cukup spesial, karena hanya diberikan kepada perempuan. 
            American Association of Women University (AAUW) memberikan bantuan pendidikan bagi para 
            perempuan yang berkuliah di AS. AAUW membuka program International Fellowships bagi mahasiswa 
            S2, S3, maupun post-doctoral. Kisaran dana pendidikan yang bakal kamu dapatkan adalah 20-50 ribu 
            dollar AS. Selain itu, AAUW juga memberikan hibah sebesar 5-7 ribu dollar AS bagi alumni International 
            Fellowships yang telah kembali ke negara asalnya. 
            </p>

            <br>
            <h5>6. Rotary Peace Fellowship</h5>
            <p class="justify">
            Program beasiswa ini memberikan hingga 100 beasiswa yang didanai penuh (uang sekolah dan biaya, 
            kamar dan asrama, transportasi pulang pergi, dan semua biaya magang dan studi lapangan) untuk para 
            siswa yang siap untuk menjadi pemimpin dan belajar di salah satu Rotary Peace Center. melalui 
            pelatihan akademik, praktik, dan peluang jaringan global, program Rotary Peace Center. Setiap tahun, 
            The Rotary Foundation memberikan hingga 50 beasiswa untuk gelar master dan 50 untuk studi sertifikat 
            di universitas-universitas terkemuka.
            </p>

            <br>
            <h5>7. The Next Gen Scholarship Fund</h5>
            <p class="justify">
            Beasiswa ini ditujukan bagi mahasiswa internasional yang bukan warga negara Amerika Serikat. 
            Selain lulusan SMA, beasiswa ini juga bisa diapply oleh kamu yang sedang berstatus mahasiswa 
            D2 sampai S1. Beasiswa dengan nominal sebesar $1,000 USD tentu akan sangat membantu studi di 
            negara mahal ini, kan? Akan tetapi, persyaratan berikutnya cukup berat nih, Sobat Pintar. Pada 
            saat mengajukan beasiswa ini, kamu harus sedang tinggal di Virginia, Maryland, atau Washington 
            DC. Ups, kamu nggak tinggal di situ? Kalau begitu, kamu harus sudah diterima di salah satu kampus 
            yang berlokasi di Virginia, Maryland, atau Washington DC.
            </p>

