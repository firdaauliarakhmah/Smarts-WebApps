            <p class="justify">
            Kabar baik bagi pemburu beasiswa kuliah di luar negeri 2023 tanpa syarat IELTS. 
            Kuliah gratis di Eropa dengan beasiswa Romanian Goverment Scholarship yang kembali dibuka.
            Pendaftaran beasiswa kuliah gratis ke Eropa tanpa syarat IELTS dan juga tidak ada batasan 
            usia untuk melanjutkan pendidikan ke luar negeri.Bagi yang ingin kuliah di universitas Rumani 
            tidak perlu khawatir mengenai biaya perkuliahannya. Pemerintah Rumania melalui Ministry 
            of Foreign Affairs (MFA) akan membiayai mahasiswa internasional yang berasal dari luar 
            negara Eropa.
            </p>
            <p class="justify">
            Tak perlu khawatir jika belum mahir bahasa Rumania, penerima beasiswa akan mendapatkan kelas 
