                            penghargaan yang diberikan kepada individu agar dapat melanjutkan pendidikan ke jenjang yang lebih tinggi. 
                            Penghargaan dapat berbentuk akses tertentu pada suatu instansi atau penghargaan berupa bantuan keuangan.</p>

