            bagi pendaftar yang akan mendaftar di jurusan kedokteran, kedokteran gigi dan farmasi. Mahasiswa 
            internasional yang berminat dengan program beasiswa ini bisa memilih kuliah dengan jenjang 
            Sarjana, Magister ataupun Dokter. Batas akhir pendaftaran beasiswa fully funded ke Rumina 
            ini ditutup pada 1 Maret 2023 dan saat ini pendaftaran sudah dibuka bagi para calon penerima 
            beasiswa. Beberapa fasilitas yang akan diterima oleh penerima beasiswa:
            </p>

