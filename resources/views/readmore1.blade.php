                            ini adalah Djarum Beasiswa Plus. Beasiswa kuliah di Indonesia yang disponsori oleh swasta 
                            cukup banyak, antara lain Beasiswa BCA, beasiswa Teladan dari Tanoto Foundation, hingga 
                            beasiswa dari perusahaan seperti PT Cloud Hosting Indonesia. Jenis beasiswa kuliah ini biasanya 
                            merupakan bentuk CSR (Corporate Social Responsibility) dari perusahaan.</p>

