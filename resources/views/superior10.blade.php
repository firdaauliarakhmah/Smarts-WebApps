              <li><i class="bi"></i> <span>Diskon transportasi dalam dan antar kota</span></li>
            </ul>

            <h5>Tentang Romanian Government Scholarship</h5>
            <p class="justify">
            Romanian Government Scholarship adalah program beasiswa yang diselenggarakan oleh Pemerintah 
            Rumania melalui Ministry of Foreign Affairs (MFA) untuk membiayai mahasiswa internasional dari 
            luar Eropa yang ingin melanjutkan kuliah di universitas di Rumania.
            Dalam rangka mempromosikan bahasa dan budaya Rumania, maka seluruh perkuliahan kecuali pada 
            program doktoral akan disampaikan dalam bahasa Rumania.
        
            Penerima beasiswa yang belum mahir berbahasa Rumania dapat mengikuti kelas pelatihan bahasa 
            Rumania selama 1 tahun sebelum memasuki perkuliahan. Program studi yang tersedia 
            di Romanian Government Scholarship 2023 meliputi:
            <ul>
              <li><i class="bi"></i> <span>Program Sarjana (S1): Program berlangsung 3–6 tahun, tergantung pilihan jurusan.</span></li>
              <li><i class="bi"></i> <span>Program Master (S2): Program berlangsung 1,5 tahun – 2 tahun.</span></li>
              <li><i class="bi"></i> <span>Program Doktoral (S3):Program berlangsung 3–4 tahun.</span></li>
            </ul>
            </p>

            <br>
            <h5>Persyaratan Romanian Government Scholarship</h5>
            <ul>
              <li><i class="bi"></i> <span>Salinan ijazah</span></li>
              <li><i class="bi"></i> <span>Salinan transkrip nilai</span></li>
              <li><i class="bi"></i> <span>Salinan akta kelahiran</span></li>
              <li><i class="bi"></i> <span>Salinan 3 halaman pertama paspor atau dokumen identitas nasional lainnya</span></li>
              <li><i class="bi"></i> <span>Curriculum vitae</span></li>
              <li><i class="bi"></i> <span>Surat Keterangan Lulus (SKL) sementara (jika belum ada ijazah)</span></li>
              <li><i class="bi"></i> <span>Surat keterangan akan lulus (jika belum ada ijazah)</span></li>
              <li><i class="bi"></i> <span>Sertifikat kemampuan bahasa Rumania (opsional)</span></li>
              <li><i class="bi"></i> <span>Surat persetujuan orang tua (bagi pendaftar yang belum berusia 18 tahun)</span></li>
              <li><i class="bi"></i> <span>Catatan : Seluruh dokumen diterjemahkan oleh penerjemah tersumpah ke dalam bahasa Inggris, Rumania, Prancis, atau Spanyol dan diunggah dalam bentuk PDF.</span></li>
