            <h5>B. Alto University Scholarship (International Undergraduate Program)</h5>
            <p class="justify">Aalto University Scholarship adalah beasiswa untuk studi di universitas Aalto, dengan pendanaan parsial atau 100% tuition waiver. Syarat yang harus diperhatikan seperti 
            Warga negara non-UE/EEA, Jika kamu dianggap bertanggung jawab atas biaya kuliah, kamu akan ditawari opsi untuk mendaftar atau tidak mengajukan Beasiswa Universitas Aalto.
            Beasiswa diberikan untuk periode normatif. Masa studi normatif adalah tiga (3) tahun studi penuh waktu untuk gelar sarjana dan dua (2) tahun studi penuh waktu untuk gelar master. 
            Beasiswa membutuhkan studi penuh waktu dan belajar sesuai dengan rencana studi pribadi yang disetujui.

