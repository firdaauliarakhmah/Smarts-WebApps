                        <h4>Tips Mendapatkan Beasiswa :</h4>
                        <h5>1. Melakukan riset dan kumpulkan informasi seputar beasiswa</h5>
                        <p class = "justify">Dalam hal ini, jika Anda masih malas untuk mencari informasi, 
                            maka sudah dipastikan Anda akan kehabisan kuota. Mencari informasi kepada pihak 
                            sekolah atau kampus bisa dikatakan sebagai cara yang terbilang cukup ampuh. Terkadang, 
                            sekolah atau kampus memiliki banyak koneksi dengan beberapa lembaga atau pihak swasta 
                            yang mengadakan beasiswa kuliah gratis. Selain itu, jangan malu untuk bertanya kepada 
                            beberapa teman atau saudara yang pernah atau sudah memiliki pengalaman dalam cara 
                            mendapatkan beasiswa kuliah gratis. Jika memang mampu, Anda juga bisa melakukan riset 
                            sendiri dengan mencari informasi dari internet atau sosial media. Dengan rajin memantau 
                            akun sosial media penyedia beasiswa ataupun melalui pencarian Google, pastinya Anda 
                            akan mendapatkan informasi lebih detail lagi.</p>
                        
                        <h5>2. Nilai Prestasi Akademik yang Mumpuni</h5>
                        <p class = "justify">Beragam jenis beasiswa memiliki persyaratan akademik yang berbeda. 
                            Ada beberapa jenis beasiswa yang mengharuskan harus memiliki nilai akademik yang mumpuni, 
                            seperti halnya Beasiswa Sampoerna Foundation yang mengharuskan nila rata-rata minimal 70. 
                            Atau jika Anda sudah menempuh jenjang S1, Anda dapat mengajukan Beasiswa KIP Kuliah yang 
                            memang mengharuskan setiap mahasiswa harus memiliki nilai akademik yang baik dan juga harus 
                            aktif dalam kegiatan kampus</p>
                        
                        <h5>3. Memenuhi Persyaratan Kualifikasi Beasiswa</h5>
                        <p class = "justify">Agar beasiswa yang diajukan tepat dengan sasaran dengan kebutuhan Anda, 
                            ada baiknya untuk mempelajari dan memenuhi semua kebutuhan persyaratan yang diberikan 
                            penyelenggara beasiswa. Namun ada satu persyaratan yang sangat umum bagi para pendaftar beasiswa. 
                            Yaitu tidak sedang menerima dari program beasiswa manapun, atau tidak sedang menerima beasiswa 
                            lain selain yang akan didaftarkan. Untuk persyaratan lainnya terkadang bisa berbeda-beda oleh 
                            setiap penyedia beasiswa kuliah gratis. Anda tinggal menyesuaikan apa saja yang menjadi 
                            persyaratannya</p>

                        <h5>4. Persiapkan Beberapa Dokumen yang Dibutuhkan</h5>
                        <p class = "justify">Selain dari persyaratan kualifikasi, calon penerima beasiswa kuliah harus 
                            bisa melengkapi beberapa persyaratan dokumen seperti KTP, SKCK dan sejenisnya. Apabila Anda 
                            memiliki beberapa sertifikat tambahan, bisa juga dilampirkan agar menambah penilaian dari penyedia 
                            beasiswa kuliah.</p>

                        <h5>5. Menguasai Bahasa Inggris Sebagai Nilai Tambah</h5>
                        <p class = "justify">Bagi siapapun, Bahasa Inggris terbilang cukup menakutkan jika memang harus dimasukkan 
                            kedalam daftar persyaratan. Namun, dengan melupakan yang satu ini mungkin dapat dikatakan sangat disayangkan 
                            karena hampir setiap Perguruan Tinggi menyukai dan bahkan melirik calon penerima beasiswa yang memiliki 
                            kemampuan bahasa Inggris yang mumpuni.</p>

