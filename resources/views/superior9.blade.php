          <center>
          <h2>Kuliah Gratis dengan Beasiswa Pendidikan LPDP</h2><br>
            <img src="img/rekom/f11.jpg" alt="" class="img-fluid services-img"> </center>
            <br>
            <p class="justify">
            Salah satu beasiswa idaman masyarakat Indonesia adalah Beasiswa LPDP. Sesuai namanya, 
            beasiswa ini diberikan oleh Lembaga Pengelola Dana Pendidikan (LPDP). LPDP adalah instansi 
            pemerintah yang berada di bawah Kementerian Keuangan RI. Setiap tahun, LPDP memberikan beasiswa 
            kepada ribuan WNI yang hendak melanjutkan studi di dalam maupun luar negeri. 
            Hal ini bertujuan untuk meningkatkan kualitas SDM Indonesia, yang nantinya bakal 
            berdampak positif pula pada perkembangan negara. 
            </p>
            <p class="justify">
            Beasiswa LPDP adalah program beasiswa yang dibiayai oleh pemerintah Indonesia 
            melalui pemanfaatan Dana Pengembangan Pendidikan Nasional (DPPN) yang dikelola 
            oleh Lembaga Pengelola Dana Pendidikan (LPDP) untuk pembiayaan pendidikan tinggi 
            pada program magister atau program doktoral di perguruan tinggi terbaik, baik dalam 
            maupun luar negeri. Beasiswa LPDP terdiri dari berbagai program, yaitu Beasiswa Afirmasi, 
            Beasiswa Targeted, Beasiswa Reguler, dan Beasiswa Kolaborasi.
            </p>

            <h5>Jenis Beasiswa LPDP</h5>
            <p class="justify">
            Beasiswa Afirmasi terdiri dari Beasiswa Berkebutuhan Khusus/Difabel, Beasiswa Daerah Afirmasi, 
            Beasiswa Prasejahtera, Beasiswa Santri. Beasiswa Targeted terbagi menjadi Beasiswa PNS, TNI, 
            dan Polri, serta Beasiswa Kewirausahaan. Beasiswa Reguler terdiri dari Beasiswa Reguler, 
            Beasiswa Perguruan Tinggi Utama Dunia (PTUD), dan Beasiswa Co-funding. Beasiswa Reguler 
            ini adalah jenis beasiswa yang umum dimanfaatkan oleh mahasiswa program magister dan doktor
            yang ingin melanjutkan pendidikan di luar negeri. Dan, Beasiswa Kolaborasi merupakan beasiswa 
            yang terkait dengan kegiatan dari Kemendikbud seperti beasiswa untuk guru, dosen, dan 
            tenaga pendidik, serta program beasiswa siswa berprestasi.
            </p>

            <br>
            <h5>Persyaratan Umum</h5>
            <p class="justify">1. Warga Negara Indonesia.</p>
            <p class="justify">2. Telah menyelesaikan studi program diploma empat (D4) atau sarjana (S1) untuk beasiswa magister; program magister (S2) untuk beasiswa doktor, atau diploma empat (D4)/sarjana (S1) langsung doktor.</p>
            <p class="justify">3. Bagi pendaftar dari diploma empat (D4)/sarjana (S1) langsung doktor wajib memenuhi ketentuan sebagai berikut:</p>
            <ul>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Memiliki LoA Unconditional dari Perguruan Tinggi tujuan, dan</span></li>
              <li><i class="justify" class="bi bi-check-circle"></i> <span>Memenuhi seluruh kriteria sebagai pendaftar program beasiswa Doktor (S3).</span></li>
            </ul>
            <p class="justify">4. Pendaftar yang telah menyelesaikan studi magister (S2) tidak diizinkan mendaftar pada program beasiswa magister dan pendaftar yang telah menyelesaikan studi doktor (S3) tidak diizinkan mendaftar pada program beasiswa doktor.</p>
            <p class="justify">5. Bagi pendaftar lulusan perguruan tinggi luar negeri pada jenjang pendidikan sebelumnya, wajib melampirkan:</p>
            <ul>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Hasil penyetaraan ijazah dan konversi IPK dari Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi, melalui laman https://ijazahln.kemdikbud.go.id/ijazahln/</span></li>
              <li><i class="justify" class="bi bi-check-circle"></i> <span>tangkapan layar ajuan penyetaraan ijazah dan konversi IPK pada laman Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi mengenai penyetaraan ijazah dan konversi IPK bagi pendaftar yang penyetaraan ijazah dan/atau konversi IPKnya belum terbit</span></li>
            </ul>
            <p class="justify">6. Tidak sedang menempuh studi (on going) program magister ataupun doktor baik di perguruan tinggi dalam negeri maupun perguruan tinggi di luar negeri.</p>
            <p class="justify">7. Tidak sedang mendaftar, akan menerima, atau menerima beasiswa dari sumber lain yang berpotensi double funding selama menjadi penerima beasiswa LPDP.</p>
            <p class="justify">8. Pendaftar yang melampirkan LoA Unconditional dengan waktu mulai studi yang tidak sesuai dengan ketentuan LPDP maka wajib melampirkan surat keterangan penundaan jadwal perkuliahan program studi dari Perguruan Tinggi yang diunggah bersamaan dengan LoA Unconditional</p>
            <p class="justify">9. Melampirkan surat rekomendasi sesuai dengan persyaratan masing-masing program.</p>
            <p class="justify">10. Bagi pendaftar berstatus PNS dan CPNS di semua program beasiswa LPDP wajib melampirkan surat usulan sekurang-kurangnya dari pejabat setingkat eselon II yang membidangi pembinaan/ pengembangan SDM pada Kementerian/ Lembaga atau Pemerintah Daerah untuk mengikuti program beasiswa LPDP kepada LPDP pada saat pendaftaran beasiswa LPDP.</p>
            <p class="justify">11. Bagi pendaftar berstatus prajurit TNI di semua program beasiswa LPDP wajib melampirkan surat usulan sekurang-kurangnya pejabat yang membidangi pembinaan SDM pada Mabes TNI/ TNI AD/ TNI AL/ TNI AU untuk mengikuti program beasiswa LPDP kepada LPDP pada saat pendaftaran beasiswa LPDP.</p>
            <p class="justify">12. Bagi pendaftar berstatus anggota POLRI di semua program beasiswa LPDP wajib melampirkan surat usulan sekurang-kurangnya pejabat yang membidangi pembinaan SDM pada Mabes POLRI untuk mengikuti program beasiswa LPDP kepada LPDP pada saat pendaftaran beasiswa LPDP.</p>
            <p class="justify">13. Memilih Perguruan Tinggi Tujuan dan program studi sesuai dengan ketentuan LPDP.</p>
            <p class="justify">14. Memiliki Indeks Prestasi Kumulatif (IPK) dan persyaratan Bahasa sesuai dengan ketentuan yang ditetapkan pada masing-masing program beasiswa.</p>
            <p class="justify">15. Beasiswa hanya diperuntukkan untuk kelas reguler atau kelas yang ditetapkan oleh LPDP, dan tidak diperuntukkan untuk kelas-kelas sebagai berikut:</p>
            <ul>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas Eksekutif</span></li>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas Khusus</span></li>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas Karyawan</span></li>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas Jarak Jauh</span></li>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas yang diselenggarakan bukan di perguruan tinggi induk</span></li>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas Internasional bagi pendaftar tujuan studi dalam negeri</span></li>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas yang diselenggarakan di lebih dari 1 (satu) negara perguruan tinggi, atau</span></li>
              <li><i class="justify"  class="bi bi-check-circle"></i> <span>Kelas lainnya yang tidak memenuhi ketentuan LPDP.</span></li>
            </ul>
            <p class="justify">16. Beasiswa dapat diberikan untuk kelas khusus dalam bentuk progam kerja sama antara LPDP dengan instansi lain.</p>
            <p class="justify">17. Menyetujui surat pernyataan yang telah disediakan pada aplikasi pendaftaran beasiswa LPDP (poin-poin terlampir).</p>
            <p class="justify">18. Menulis profil diri termasuk riwayat pendidikan yang tidak diselesaikan pada aplikasi pendaftaran.</p>
            <p class="justify">19. Menulis komitmen kembali ke Indonesia, rencana pasca studi, dan rencana kontribusi di Indonesia.</p>
            <p class="justify">20. Menulis Proposal Penelitian bagi pendaftar program pendidikan doktor.</p>
            <p class="justify">21. Jika pendaftar memiliki publikasi ilmiah, prestasi kejuaraan/non kejuaraan, dan pengalaman organisasi maka pendaftar mengisi riwayat publikasi ilmiah, prestasi kejuaraan/non kejuaraan, dan pengalaman organisasi pada aplikasi pendaftaran.</p>

