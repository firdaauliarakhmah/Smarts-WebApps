          <center>
          <h2>Rekomendasi Beasiswa Terpopular Di Indonesia</h2><br>
            <img src="img/rekom/f3.jpg" alt="" class="img-fluid services-img"> </center>
            <br>
            <p class="justify">
            Melanjutkan pendidikan di perguruan tinggi merupakan keinginan banyak pelajar didunia, namun tidak 
            semua orang bisa melanjutkan pendidikannya sampai di bangku pendidikan kuliah.
            Melanjutkan pendidikan dibangku kuliah menjadi salah satu mimpi terbesar dari banyak mimpi yang telah dirangkai 
            pelajar yang ada didunia, apalagi jika di ikuti dengan sebuah privilege sebagai mahasiswa yang mendapatkan beasiswa.
            Selain bisa membantu meringankan beban kedua orang tua, beasiswa juga dapat menjadi suatu penyemangat 
            belajar selama berada di bangku perguruan tinggi. Sehingga dapat meningkatkan kreativitas 
            dan prestasi mahasiswa di bidang akademik dan non-akademik. 
            Berikut ini adalah beberapa daftar rekomendasi beasiswa paling populer yang telah kami ringkas:  
            </p>
            <h5>1. Bidikmisi</h5>
            <p class="justify">
                Bidikmisi adalah bantuan biaya pendidikan dari pemerintah bagi lulusan Sekolah 
                Menengah Atas (SMA) atau sederajat yang memiliki potensi akademik baik tetapi 
                memiliki keterbatasan ekonomi. Berbeda dari beasiswa yang berfokus pada memberikan 
                penghargaan atau dukungan dana terhadap mereka yang berprestasi (dilihat dari penjelasan Pasal 
                76 UU No. 12 Tahun 2012 tentang Pendidikan Tinggi). Walaupun demikian, syarat prestasi 
                pada bidikmisi ditujukan untuk menjamin bahwa penerima bidikmisi terseleksi dari yang 
                benar benar mempunyai potensi dan kemauan untuk menyelesaikan pendidikan tinggi. 
            </p>
            <p class="justify">
                Bidikmisi adalah program dari Kemendikbudristek yang ditujukan untuk siswa kurang mampu 
                agar dapat melanjutkan studi ke perguruan tinggi, beasiswa ini hanya menyasar ke perguruan 
                dalam negeri saja sehingga sangat terbatas kampus-kampusnya. 
                Jika kamu diterima beasiswa Bidikmisi, maka kamu akan mendapatkan gratis pembiayaan kuliah, 
                uang saku bulanan dan beberapa acara pembinaan karakter dari Kemendikbud. Adapun beberapa syarat yang harus dipenuhi seperti: 
            </p>
            <ul>
              <li><i class="bi"></i> <span>Siswa SMA/sederajat yang hendak lulus pada tahun berlangsung atau lulus satu tahun sebelumnya</span></li>
              <li><i class="bi"></i> <span>Memiliki potensi akademik yang baik/unggul</span></li>
              <li><i class="bi"></i> <span>Memiliki kekurangan dalam hal ekonomi (didukung dokumen yang sah)</span></li>
              <li><i class="bi"></i> <span>Lulus seleksi PMB pada perguruan tinggi</span></li>
            </ul>

                <a href="https://bidikmisi.belmawa.ristekdikti.go.id/" class="btn btn-info py-sm-1 px-sm-5 me-1">Selengkapnya...</a>
                <br>
            <br>
            <h5>2. Lembaga Pengelola Dana Pendidikan (LPDP)</h5>
            <p class="justify">Beasiswa LPDP adalah program beasiswa yang dibiayai oleh pemerintah Indonesia 
                melalui pemanfaatan Dana Pengembangan Pendidikan Nasional (DPPN) yang dikelola oleh Lembaga 
                Pengelola Dana Pendidikan (LPDP) untuk pembiayaan pendidikan tinggi pada program magister 
                atau program doktoral di perguruan tinggi terbaik, baik dalam maupun luar negeri.
                Beasiswa LPDP terdiri dari berbagai program, yaitu Beasiswa Afirmasi, Beasiswa Targeted, 
                Beasiswa Reguler, dan Beasiswa Kolaborasi.
            </p>
            <p class="justify" >Lembaga Pengelola Dana Pendidikan (LPDP) merupakan beasiswa yang diselenggarakan oleh 
                Kementerian Keuangan RI. Beasiswa ini cukup populer dikalangan mahasiswa yang ingin 
                melanjutkan studinya ke jenjang S2 atau S3, terutama kampus diluar negeri.  
                Meski begitu, melalui beasiswa LPDP kamu juga bisa mengajukan beasiswa di 
                kampus dalam negeri. Untuk penerima beasiswa LPDP, kamu akan mendapatkan tunjangan keluarga, 
                tunjangan kedatangan, tunjangan darurat, biaya hidup bulanan, asuransi kesehatan dasar, 
                akomodasi PP Bahkan hingga urusan visa jika kamu kuliah diluar negeri. </p>
                <a href="https://lpdp.kemenkeu.go.id/" class="btn btn-info py-sm-1 px-sm-5 me-1">Selengkapnya...</a>
                <br>

            <br>
            <h5>3. Kemenag (Kementrian Agama) RI</h5>
            <p class="justify">Beasiswa ini merupakan program yang diselenggarakan dari kementerian 
              Agama atau Kemenag RI. Beasiswa ini rata-rata diperuntukkan untuk para lulusan santri.  
              Tujuan diadakan beasiswa ini, yaitu mencapai bibit-bibit penerus bangsa yang bisa membangun 
              kualitas intelektual yang religius dan cinta akan bangsanya. </p>
              <p class="justify">
              Kementrian agama juga mengadakan Beasiswa indonesia bangkit (BIB). Beasiswa Indonesia Bangkit 
              (BIB) merupakan skema beasiswa prestisius kolaborasi dari Kementerian Agama dan LPDP 
              Kementerian Keuangan untuk masyarakat Indonesia yang ingin mengembangkan karier, pengalaman, 
              dan jaringan kampus di dalam dan luar negeri. Melalui program gelar (degree program) maupun 
              non gelar (non degree program), Beasiswa Indonesia Bangkit berkomitmen untuk mempersiapkan 
              pemimpin, pendidik, dan profesional masa depan serta mendorong inovasi demi terwujudnya 
              Indonesia yang rukun, harmonis, dan sejahtera. BIB terus bergerak menuju kualitas sumber 
              daya manusia yang memiliki daya saing tinggi, bukan hanya pada skala lokal, namun juga skala 
              regional bahkan internasional. </p>
              <p class="justify">
              Pengembangan kualitas sumber daya manusia merupakan salah satu program prioritas Pemerintah 
              sebagaimana tercantum dalam Rencana Pembangunan Jangka Menengah Nasional (RPJMN) 2020-2024. 
              Kementerian Agama juga memberikan perhatian penting pada program pengembangan kualitas SDM 
              secara berkelanjutan dalam rangka mendukung target Rencana Strategis Kementerian Agama 
              2020-2024, terutama dalam bidang peningkatan SDM bidang pendidikan agama dan keagamaan.</p>
              <P class="justify">
              Dalam rangka mendukung tujuan di atas, Kementerian Agama bekerja sama dengan Lembaga 
              Pengelola Dana Pendidikan (LPDP) Kementerian Keuangan melaksanakan program peningkatan 
              kapasitas SDM melalui Program Pendanaan Beasiswa Kementerian Agama mulai Tahun 2022
            </p>
